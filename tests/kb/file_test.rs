#[cfg(feature = "kb-office")]
mod office {
    use super::*;

    use docx_rs::{Docx, Paragraph, Run};

    fn build_docx_bytes(paragraphs: &[&str]) -> Vec<u8> {
        let mut doc = Docx::new();
        for body in paragraphs {
            doc = doc.add_paragraph(Paragraph::new().add_run(Run::new().add_text(*body)));
        }
        let xml = doc.build();
        let mut buf = std::io::Cursor::new(Vec::<u8>::new());
        docx_rs::zip(&mut buf, xml).expect("zip docx");
        buf.into_inner()
    }

    #[tokio::test]
    async fn process_office_docx_extracts_text() {
        let cfg = make_kb_cfg("http://127.0.0.1:1/unused".into());
        let dir = tempfile::tempdir().unwrap();
        let path = dir.path().join("rantaiclaw_doc.docx");
        let bytes = build_docx_bytes(&["First paragraph rantaiclaw.", "Second paragraph body."]);
        std::fs::write(&path, bytes).unwrap();

        let r = process_file(&cfg, &path, ProcessingOptions::default())
            .await
            .unwrap();
        assert_eq!(r.file_type, SupportedFileType::Document);
        assert!(
            r.content.contains("First paragraph rantaiclaw."),
            "first paragraph missing: {:?}",
            r.content
        );
        assert!(
            r.content.contains("Second paragraph body."),
            "second paragraph missing: {:?}",
            r.content
        );
    }

    /// Committed fixture: `tests/fixtures/sample.xlsx`. Hand-crafted to keep
    /// the integration self-contained; `calamine` is reader-only.
    fn ensure_xlsx_fixture() -> PathBuf {
        fixtures_dir().join("sample.xlsx")
    }

    #[tokio::test]
    async fn process_office_xlsx_extracts_sheet_content() {
        let cfg = make_kb_cfg("http://127.0.0.1:1/unused".into());
        let path = ensure_xlsx_fixture();
        assert!(
            path.exists(),
            "tests/fixtures/sample.xlsx must be committed for this test"
        );

        let r = process_file(&cfg, &path, ProcessingOptions::default())
            .await
            .unwrap();
        assert_eq!(r.file_type, SupportedFileType::Document);
        // Output convention: `## Sheet: <name>` headers + TSV rows.
        assert!(
            r.content.contains("## Sheet:"),
            "sheet header missing in xlsx output: {:?}",
            r.content
        );
        assert!(
            r.content.to_lowercase().contains("hello"),
            "expected fixture cell text 'hello' missing: {:?}",
            r.content
        );
    }

    #[tokio::test]
    async fn process_office_returns_error_for_unsupported_extension() {
        let cfg = make_kb_cfg("http://127.0.0.1:1/unused".into());
        let dir = tempfile::tempdir().unwrap();
        let path = dir.path().join("deck.pptx");
        // `.pptx` is in DOCUMENT_EXTENSIONS so the dispatcher routes here,
        // but office.rs returns UnsupportedFileType for pptx specifically.
        std::fs::write(&path, b"not a real pptx").unwrap();
        let err = process_file(&cfg, &path, ProcessingOptions::default())
            .await
            .expect_err("pptx must surface UnsupportedFileType");
        assert!(
            matches!(err, KbError::UnsupportedFileType(_)),
            "expected UnsupportedFileType, got: {err}"
        );
    }
}
