#[test]
fn scan_directory_finds_supported_files_recursively() {
    let dir = tempfile::tempdir().unwrap();
    let root = dir.path();
    let nested = root.join("nested");
    std::fs::create_dir(&nested).unwrap();

    let md = root.join("a.md");
    let pdf = nested.join("b.pdf");
    let bad = nested.join("c.unsupported");
    std::fs::write(&md, "x").unwrap();
    std::fs::write(&pdf, "x").unwrap();
    std::fs::write(&bad, "x").unwrap();

    let mut found = scan_directory(root);
    found.sort();
    let mut expected = vec![md.clone(), pdf.clone()];
    expected.sort();
    assert_eq!(found, expected, "scan must return md + pdf, not unsupported");
}

#[test]
fn scan_directory_returns_empty_for_missing_dir() {
    let dir = tempfile::tempdir().unwrap();
    let missing = dir.path().join("nope");
    assert!(file::scan_directory(&missing).is_empty());
}

